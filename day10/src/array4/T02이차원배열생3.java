package array4;

public class T02이차원배열생3 {

	public static void main(String[] args) {

		float [][] qwqw = new float[][] {
			{133.3f , 172.2f},
			{37.2f , 88f }
		};
		
		for(int k=0;  k <qwqw.length; k++) {
			for(int i=0; i <qwqw[k].length; i++) {
				System.out.print(qwqw[k][i] + " \t");
		}System.out.println();
		
		
		}
	}

}
