package data2;

public class Test01실수 {

	public static void main(String[] args) {
		//실수란 소수점이있는수 
		//고정소수점방식 부동소수점방식 다시찾아서공부할것
		//자바는 부동소수점 손해발생하더라도 더용량이작음
		//double이 기본이고 float는 더 작은용량으로 더적게만표현가능
		double a = 123.4567;
		System.out.println(a);
		float b = 12.11f;
		System.out.println(b);
		//long과 마찬가지로 float 를사용할땐 f붙여야함
		//정수+정수= 정수 , 정수+실수 = 실수  
		//평균구하기
		double aa = 4;
		double bb = 7;
		double cc = (aa+bb)/2;
		System.out.println(cc);
			
		
	

	
	}
	

}
