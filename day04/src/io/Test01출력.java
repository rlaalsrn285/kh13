package io;

import java.util.Scanner;

public class Test01출력 {

	public static void main(String[] args) {
		//표준출력통로 System.out 시스템의 표준출력장치
		//println 출력후 줄바꿈
		//print 글자만 출력
		//표준입력통로 System.in
		//표준입력 standard input
		//표준입력장치 키보드
		//system.in 표준입력
		
		Scanner sc = new Scanner (System.in); // 입력도구생성
		
		int a = sc.nextInt(); // 키보드에서 숫자한개를 받아 a에1 저장
		
		System.out.println(a +"ㅂ");
		
		

	}

}
